/**
 * Lemma Central API Configuration Engine
 */
const APIConfigManager = {
    DEFAULT_PROD_URL: 'https://r4hul-78-lemma-backend.hf.space',
    DEFAULT_DEV_URL: 'http://localhost:8000',
    STORAGE_KEY: 'lemma_override_api_url',

    /**
     * Resolves the primary URL used to reach the FastAPI backend.
     * Supports production HF space, standard local dev, Capacitor WebViews,
     * and custom local-network IP overrides for device testing.
     */
    async getApiBaseUrl() {
        // 1. Check for manual developer settings overrides (saved in LocalStorage)
        const localOverride = localStorage.getItem(this.STORAGE_KEY);
        if (localOverride) {
            return localOverride;
        }

        // 2. Detect if running inside a Capacitor/Native WebView container
        const isCapacitor = window.Capacitor !== undefined || window.location.href.startsWith('file:');

        // 3. Fallback dynamically based on browser location
        const host = window.location.hostname;
        if (host === 'localhost' || host === '127.0.0.1') {
            if (isCapacitor) {
                // If running in Capacitor webview, point to local server IP or default to production Space.
                // Developers can use the Dev Panel switcher inside the app to adjust this.
                return this.DEFAULT_PROD_URL;
            }
            return this.DEFAULT_DEV_URL;
        }

        // 4. Read runtime configurations generated by host deployments (Vercel)
        try {
            // Must fetch with a timeout to avoid blocking page load if config is missing
            const controller = new AbortController();
            const timeoutId = setTimeout(() => controller.abort(), 2000);
            
            const response = await fetch('config.json', { signal: controller.signal });
            clearTimeout(timeoutId);
            
            if (response.ok) {
                const configData = await response.json();
                if (configData.BACKEND_API_URL) {
                    return configData.BACKEND_API_URL;
                }
            }
        } catch (e) {
            console.warn("Could not retrieve config.json, using default API:", e);
        }

        return this.DEFAULT_PROD_URL;
    },

    /**
     * Saves a developer-specific API base URL (e.g. http://192.168.1.100:8000)
     */
    setDeveloperOverrideUrl(url) {
        if (!url) {
            localStorage.removeItem(this.STORAGE_KEY);
            return;
        }
        localStorage.setItem(this.STORAGE_KEY, url);
    }
};
